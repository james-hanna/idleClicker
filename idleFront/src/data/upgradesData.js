function generateTableGameUpgrades(
  startLevel,
  startCost,
  costMultiplier,
  incomeMultiplierStart,
  incomeMultiplierIncrement,
  visitorRate,
  idleIncomeRate,
  levels,
  names = []
) {
  return Array.from({ length: levels }, (_, i) => ({
    level: startLevel + i,
    cost: startCost * Math.pow(costMultiplier, i),
    incomeMultiplier: incomeMultiplierStart + incomeMultiplierIncrement * i,
    visitorRate,
    idleIncomeRate,
    name: names[i] || `Level ${startLevel + i}`,
  }));
}
function generateTableGameTables(
  startLevel,
  startCost,
  costMultiplier,
  incomeMultiplierStart,
  incomeMultiplierIncrement,
  visitorRate,
  idleIncomeRate,
  seats
) {
  return Array.from({ length: seats }, (_, i) => ({
    seat: 1 + i,
    level: startLevel,
    cost: startCost * Math.pow(costMultiplier, i),
    incomeMultiplier: incomeMultiplierStart + incomeMultiplierIncrement * i,
    visitorRate,
    idleIncomeRate,
  }));
}

function generateFurniture(
  type,
  levels,
  costBase,
  costMultiplier,
  incomeMultiplierBase,
  incomeMultiplierStep
) {
  return Array.from({ length: levels }, (_, i) => ({
    level: i + 1,
    name: `${type} Level ${i + 1}`,
    cost: costBase * Math.pow(costMultiplier, i),
    incomeMultiplier: incomeMultiplierBase + incomeMultiplierStep * i,
    visitorRate: 1,
    idleIncomeRate: 1,
  }));
}

